create or replace PROCEDURE EJECUTAR_CONSULTAS AS
    v_cursor SYS_REFCURSOR;
    v_cedula NUMBER := 123456789; -- Ejemplo de cedula
    v_id_materia NUMBER := 1; -- Ejemplo de ID materia

    -- Variables para mostrar resultados
    v_materia VARCHAR2(100);
    v_fecha DATE;
    v_hora TIMESTAMP;
    v_aula NUMBER;
    v_motivo VARCHAR2(100);
    v_periodos NUMBER;
    v_fecha_cong DATE;
    v_nombre_comp VARCHAR2(200);
    v_tipo_adec VARCHAR2(150);
    v_obs VARCHAR2(200);
    v_calificacion NUMBER;
    v_estado VARCHAR2(20);
BEGIN
    -- 1. Consultar horarios de un alumno
    DBMS_OUTPUT.PUT_LINE('=== HORARIOS DEL ALUMNO ===');
    SP_CONSULTAR_HORARIOS_ALUMNO(v_cedula, v_cursor);
    LOOP
        FETCH v_cursor INTO v_materia, v_fecha, v_hora, v_aula;
        EXIT WHEN v_cursor%NOTFOUND;
        DBMS_OUTPUT.PUT_LINE('Materia: ' || NVL(v_materia,'') || 
                             ', Fecha: ' || NVL(TO_CHAR(v_fecha, 'DD/MM/YYYY'),'') || 
                             ', Hora: ' || NVL(TO_CHAR(v_hora, 'HH24:MI'),'') || 
                             ', Aula: ' || NVL(TO_CHAR(v_aula),''));
    END LOOP;
    CLOSE v_cursor;

    -- 2. Consultar materias congeladas
    DBMS_OUTPUT.PUT_LINE('=== MATERIAS CONGELADAS ===');
    SP_MATERIAS_CONGELADAS_ALUMNO(v_cedula, v_cursor);
    LOOP
        FETCH v_cursor INTO v_materia, v_motivo, v_periodos, v_fecha_cong;
        EXIT WHEN v_cursor%NOTFOUND;
        DBMS_OUTPUT.PUT_LINE('Materia: ' || NVL(v_materia,'') || 
                             ', Motivo: ' || NVL(v_motivo,'') || 
                             ', Periodos: ' || NVL(TO_CHAR(v_periodos),'') || 
                             ', Fecha: ' || NVL(TO_CHAR(v_fecha_cong, 'DD/MM/YYYY'),''));
    END LOOP;
    CLOSE v_cursor;

    -- 3. Consultar estudiantes con adecuacion
    DBMS_OUTPUT.PUT_LINE('=== ESTUDIANTES CON ADECUACION ===');
    SP_ESTUDIANTES_CON_ADECUACION(v_cursor);
    LOOP
        FETCH v_cursor INTO v_cedula, v_nombre_comp, v_tipo_adec, v_obs;
        EXIT WHEN v_cursor%NOTFOUND;
        DBMS_OUTPUT.PUT_LINE('Estudiante: ' || NVL(v_nombre_comp,'') || 
                             ' (' || NVL(TO_CHAR(v_cedula),'') || '), ' || 
                             'Adecuacion: ' || NVL(v_tipo_adec,'') || 
                             ', Observaciones: ' || NVL(v_obs,''));
    END LOOP;
    CLOSE v_cursor;

    -- 4. Consultar aprobados/reprobados
    DBMS_OUTPUT.PUT_LINE('=== APROBADOS/REPROBADOS ===');
    SP_ESTUDIANTES_APROBADOS_REPROBADOS(v_id_materia, v_cursor);
    LOOP
        FETCH v_cursor INTO v_materia, v_cedula, v_nombre_comp, v_calificacion, v_estado, v_obs;
        EXIT WHEN v_cursor%NOTFOUND;
        DBMS_OUTPUT.PUT_LINE('Materia: ' || NVL(v_materia,'') || 
                             ', Estudiante: ' || NVL(v_nombre_comp,'') || 
                             ', Calificacion: ' || NVL(TO_CHAR(v_calificacion),'') || 
                             ', Estado: ' || NVL(v_estado,''));
    END LOOP;
    CLOSE v_cursor;
END;
/

