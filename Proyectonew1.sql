create or replace PROCEDURE SP_INSERTAR_ESTUDIANTE(
    p_cedula        IN NUMBER,
    p_nombre        IN VARCHAR2,
    p_apellido1     IN VARCHAR2,
    p_apellido2     IN VARCHAR2,
    p_fecha_nacimiento IN DATE,
    p_telefono      IN VARCHAR2,
    p_email         IN VARCHAR2,
    p_provincia     IN VARCHAR2,
    p_tipo_id       IN NUMBER
)
IS
    v_id_telefono   NUMBER;
    v_id_email      NUMBER;
    v_id_direccion  NUMBER;
    v_id_provincia  NUMBER;
    v_count         NUMBER;
BEGIN
    -- Validar si la cédula ya existe
    SELECT COUNT(*) INTO v_count
    FROM FIDE_PERSONAS_TB
    WHERE CEDULA = p_cedula;

    IF v_count > 0 THEN
        RAISE_APPLICATION_ERROR(-20005, 'La cédula ya existe: ' || p_cedula);
    END IF;

    -- Buscar o insertar el teléfono
    BEGIN
        SELECT ID_TELEFONO INTO v_id_telefono
        FROM FIDE_TELEFONOS_TB
        WHERE TELEFONO = p_telefono AND ROWNUM = 1;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            SELECT NVL(MAX(ID_TELEFONO), 0) + 1 INTO v_id_telefono FROM FIDE_TELEFONOS_TB;
            INSERT INTO FIDE_TELEFONOS_TB (ID_TELEFONO, TELEFONO, ESTADO_ID, ESTADO, ACCION)
            VALUES (v_id_telefono, p_telefono, 1, 'ACTIVO', 'Creacion');
    END;

    -- Buscar o insertar el email
    BEGIN
        SELECT ID_EMAIL INTO v_id_email
        FROM FIDE_EMAIL_TB
        WHERE CORREO_ELECTRONICO = p_email AND ROWNUM = 1;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            SELECT NVL(MAX(ID_EMAIL), 0) + 1 INTO v_id_email FROM FIDE_EMAIL_TB;
            INSERT INTO FIDE_EMAIL_TB (ID_EMAIL, CORREO_ELECTRONICO, ESTADO_ID, ESTADO, ACCION)
            VALUES (v_id_email, p_email, 1, 'ACTIVO', 'Creacion');
    END;

    -- Validar existencia de provincia
    BEGIN
        SELECT ID_PROVINCIA INTO v_id_provincia 
        FROM FIDE_PROVINCIA_TB 
        WHERE NOMBRE_PROVINCIA = p_provincia;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            RAISE_APPLICATION_ERROR(-20002, 'Provincia no encontrada: ' || p_provincia);
    END;

    -- Insertar en FIDE_PERSONAS_TB
    INSERT INTO FIDE_PERSONAS_TB (
        CEDULA, NOMBRE, PRIMER_APELLIDO, SEGUNDO_APELLIDO,
        FECHA_NACIMIENTO, TELEFONO_ID, EMAIL_ID, ESTADO_ID,
        ESTADO, ACCION, TIPO_ID
    )
    VALUES (
        p_cedula, p_nombre, p_apellido1, p_apellido2,
        p_fecha_nacimiento, v_id_telefono, v_id_email, 1,
        'ACTIVO', 'Creacion', p_tipo_id
    );

    -- Insertar en FIDE_ESTUDIANTES_TB
    INSERT INTO FIDE_ESTUDIANTES_TB (CEDULA, ESTADO_ID, ESTADO, ACCION)
    VALUES (p_cedula, 1, 'ACTIVO', 'Creacion');

    -- Insertar en FIDE_DIRECCIONES_TB
    SELECT NVL(MAX(ID_DIRECCION), 0) + 1 INTO v_id_direccion FROM FIDE_DIRECCIONES_TB;
    INSERT INTO FIDE_DIRECCIONES_TB (
        ID_DIRECCION, CEDULA, PROVINCIA_ID,
        ESTADO_ID, ESTADO, ACCION, CREATED_BY, CREATION_DATE
    )
    VALUES (
        v_id_direccion, p_cedula, v_id_provincia,
        1, 'ACTIVO', 'Creacion', USER, SYSDATE
    );

    -- Actualizar FIDE_PERSONAS_TB con la dirección
    UPDATE FIDE_PERSONAS_TB
    SET DIRECCIONES_ID = v_id_direccion
    WHERE CEDULA = p_cedula;

    COMMIT;
    DBMS_OUTPUT.PUT_LINE('Estudiante insertado correctamente.');

EXCEPTION
    WHEN OTHERS THEN
        ROLLBACK;
        RAISE_APPLICATION_ERROR(-20001, 'Error en SP_INSERTAR_ESTUDIANTE: ' || SQLERRM);
END SP_INSERTAR_ESTUDIANTE;
