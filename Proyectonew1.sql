create or replace PROCEDURE SP_MATRICULAR_MATERIA (
    p_cedula     IN NUMBER,
    p_id_materia IN NUMBER,
    p_id_horario IN NUMBER
)
AS
    v_existente        NUMBER := 0;
    v_estado_materia   VARCHAR2(50);
    v_estado_id        NUMBER := 1;
    v_id_em            NUMBER;
BEGIN
    -- Verificar si ya esta matriculado activamente
    SELECT COUNT(*) INTO v_existente
    FROM FIDE_ESTUDIANTE_MATERIA_TB
    WHERE ID_ESTUDIANTE = p_cedula
      AND ID_MATERIA = p_id_materia
      AND ESTADO = 'ACTIVO';

    IF v_existente > 0 THEN
        RAISE_APPLICATION_ERROR(-20001, 'El estudiante ya esta matriculado en esta materia.');
    END IF;

    -- Verificar si la materia esta congelada
    BEGIN
        SELECT ESTADO INTO v_estado_materia
        FROM FIDE_ESTUDIANTE_MATERIA_TB
        WHERE ID_ESTUDIANTE = p_cedula
          AND ID_MATERIA = p_id_materia
          AND ESTADO = 'CONGELADO'
          AND ROWNUM = 1;

        IF v_estado_materia = 'CONGELADO' THEN
            RAISE_APPLICATION_ERROR(-20002, 'La materia esta congelada para este estudiante.');
        END IF;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            NULL; -- Continua si no hay congelamiento previo
    END;

    -- Obtener nuevo ID_ESTUDIANTE_MATERIA
    SELECT NVL(MAX(ID_EM), 0) + 1 INTO v_id_em FROM FIDE_ESTUDIANTE_MATERIA_TB;

    -- Insertar la nueva matricula
    INSERT INTO FIDE_ESTUDIANTE_MATERIA_TB (
        ID_EM, ID_ESTUDIANTE, ID_MATERIA,
        ESTADO_ID, ESTADO, ACCION
    ) VALUES (
        v_id_em, p_cedula, p_id_materia,
        v_estado_id, 'ACTIVO', 'Matricula creada desde formulario web'
    );

    COMMIT;
    DBMS_OUTPUT.PUT_LINE('Matricula creada exitosamente.');

EXCEPTION
    WHEN OTHERS THEN
        ROLLBACK;
        RAISE_APPLICATION_ERROR(-20003, 'Error en SP_MATRICULAR_MATERIA: ' || SQLERRM);
END SP_MATRICULAR_MATERIA;
