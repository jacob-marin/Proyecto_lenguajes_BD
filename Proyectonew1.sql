create or replace PROCEDURE SP_ACTUALIZAR_NOTA(
    p_cedula IN NUMBER,
    p_calificacion IN NUMBER,
    p_porcentaje IN NUMBER,
    p_observaciones IN VARCHAR2
)
IS
    v_existe    NUMBER := 0;
    v_nota_id   NUMBER;
BEGIN
    -- Verificar si el estudiante existe y tiene al menos una materia asociada
    SELECT COUNT(*) INTO v_existe
    FROM FIDE_ESTUDIANTES_TB E
    JOIN FIDE_ESTUDIANTE_MATERIA_TB EM ON E.CEDULA = EM.ID_ESTUDIANTE
    WHERE E.CEDULA = p_cedula;

    IF v_existe = 0 THEN
        DBMS_OUTPUT.PUT_LINE('El estudiante no tiene materias asignadas.');
        RETURN;
    END IF;

    -- Verificar si ya tiene una nota asignada
    SELECT E.NOTAS_ID INTO v_nota_id
    FROM FIDE_ESTUDIANTES_TB E
    WHERE E.CEDULA = p_cedula AND ROWNUM = 1;

    IF v_nota_id IS NOT NULL THEN
        -- Actualizar la nota existente
        UPDATE FIDE_NOTAS_TB
        SET CALIFICACION = p_calificacion,
            PORCENTAJE = p_porcentaje,
            OBSERVACIONES = p_observaciones,
            LAST_UPDATE_DATE = SYSDATE
        WHERE ID_NOTAS = v_nota_id;

        DBMS_OUTPUT.PUT_LINE('Nota actualizada correctamente.');
    ELSE
        -- Insertar nueva nota y actualizar ID en el estudiante
        SELECT NVL(MAX(ID_NOTAS), 0) + 1 INTO v_nota_id FROM FIDE_NOTAS_TB;

        INSERT INTO FIDE_NOTAS_TB (
            ID_NOTAS, ESTADO_ID, CALIFICACION, PORCENTAJE, OBSERVACIONES, ESTADO
        ) VALUES (
            v_nota_id, 1, p_calificacion, p_porcentaje, p_observaciones, 'ACTIVO'
        );

        UPDATE FIDE_ESTUDIANTES_TB
        SET NOTAS_ID = v_nota_id
        WHERE CEDULA = p_cedula;

        DBMS_OUTPUT.PUT_LINE('Nota registrada y asociada correctamente.');
    END IF;

EXCEPTION
    WHEN NO_DATA_FOUND THEN
        DBMS_OUTPUT.PUT_LINE('No se encontró el estudiante.');
    WHEN OTHERS THEN
        DBMS_OUTPUT.PUT_LINE('Error al actualizar nota: ' || SQLERRM);
END SP_ACTUALIZAR_NOTA;
/

create or replace PROCEDURE SP_CONSULTAR_HORARIOS_ALUMNO (
    p_cedula IN NUMBER,
    p_cursor OUT SYS_REFCURSOR
) AS
BEGIN
    OPEN p_cursor FOR
    SELECT m.NOMBRE AS MATERIA, h.FECHA, h.HORA, a.AULA AS NUMERO_AULA
    FROM FIDE_ESTUDIANTE_MATERIA_TB em
    JOIN FIDE_MATERIA_TB m ON em.ID_MATERIA = m.ID_MATERIA
    JOIN FIDE_MATERIAS_HORARIOS_TB mh ON m.ID_MATERIA = mh.ID_MATERIA
    JOIN FIDE_HORARIO_TB h ON mh.ID_HORARIO = h.ID_HORARIO
    JOIN FIDE_AULA_TB a ON h.AULA_ID = a.ID_AULA
    WHERE em.ID_ESTUDIANTE = p_cedula
    ORDER BY h.FECHA, h.HORA;

    DBMS_OUTPUT.PUT_LINE('Horarios consultados exitosamente para el estudiante: ' || p_cedula);
EXCEPTION
    WHEN OTHERS THEN
        DBMS_OUTPUT.PUT_LINE('Error al consultar horarios: ' || SQLERRM);
END;
/

