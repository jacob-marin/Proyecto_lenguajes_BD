create or replace PROCEDURE SP_ESTUDIANTES_APROBADOS_REPROBADOS (
    p_id_materia IN NUMBER DEFAULT NULL,
    p_cursor OUT SYS_REFCURSOR
) AS
BEGIN
    OPEN p_cursor FOR
    SELECT m.NOMBRE AS MATERIA, 
           p.CEDULA, 
           p.NOMBRE || ' ' || p.PRIMER_APELLIDO AS ESTUDIANTE,
           n.CALIFICACION,
           CASE WHEN n.CALIFICACION >= 70 THEN 'APROBADO' ELSE 'REPROBADO' END AS ESTADO,
           n.OBSERVACIONES
    FROM FIDE_NOTAS_TB n
    JOIN FIDE_ESTUDIANTES_TB e ON n.ID_NOTAS = e.NOTAS_ID
    JOIN FIDE_PERSONAS_TB p ON e.CEDULA = p.CEDULA
    JOIN FIDE_ESTUDIANTE_MATERIA_TB em ON e.CEDULA = em.ID_ESTUDIANTE
    JOIN FIDE_MATERIA_TB m ON em.ID_MATERIA = m.ID_MATERIA
    WHERE (p_id_materia IS NULL OR m.ID_MATERIA = p_id_materia)
    ORDER BY m.NOMBRE, 
